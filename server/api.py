@app.post("/api/easy-mode")
async def easy_mode(file: UploadFile = File(...)):
    """Extract text from PDF, summarize into bullet points using LLM, and generate PPT."""
    # 1. Save uploaded file
    tmp_dir = Path("data/tmp")
    tmp_dir.mkdir(parents=True, exist_ok=True)
    pdf_path = tmp_dir / f"{uuid.uuid4()}.pdf"
    
    try:
        with open(pdf_path, "wb") as buffer:
            shutil.copyfileobj(file.file, buffer)
        
        # 2. Extract text
        text = extract_text(str(pdf_path))
        if not text:
            raise HTTPException(status_code=400, detail="Could not extract text from PDF")
        
        # 3. Use LLM to extract bullet points
        llm = LLMClient()
        prompt = f"""Analyze this machine learning research paper and extract the following information for a presentation:
1. A catchy title for the presentation.
2. A professional 3-4 sentence description/summary of the paper.
3. 5-7 concise bullet points highlight key contributions, methodology, and results.

Return ONLY a JSON object with keys: "title", "description", and "bullets" (array of strings).

Paper text (first 6000 chars):
{text[:6000]}"""
        
        response = llm.generate_structured(prompt)
        
        paper_title = response.get("title", "Research Paper Summary")
        paper_desc = response.get("description", "Failed to generate a description.")
        bullets = response.get("bullets", ["No highlights found."])
        
        if not isinstance(bullets, list):
            bullets = [str(bullets)]
            
        # 4. Generate PPT
        prs = Presentation()
        
        # 1. Title Slide
        title_slide_layout = prs.slide_layouts[0]
        slide = prs.slides.add_slide(title_slide_layout)
        
        # Add some color to the background (Rectangles are used as backgrounds in python-pptx)
        left = top = 0
        width = prs.slide_width
        height = prs.slide_height
        rect = slide.shapes.add_shape(
            1, left, top, width, height
        )
        rect.fill.solid()
        rect.fill.fore_color.rgb = RGBColor(26, 31, 44) # Dark Ink background
        rect.line.fill.background()

        title = slide.shapes.title
        subtitle = slide.placeholders[1]
        title.text = paper_title
        title.text_frame.paragraphs[0].font.color.rgb = RGBColor(255, 255, 255)
        title.text_frame.paragraphs[0].font.bold = True

        subtitle.text = "Generated by ReproAgent · AI Research Assistant"
        subtitle.text_frame.paragraphs[0].font.color.rgb = RGBColor(139, 92, 246) # Accent Violet
        
        # 2. Description Slide
        desc_slide_layout = prs.slide_layouts[1]
        slide2 = prs.slides.add_slide(desc_slide_layout)
        slide2.shapes.title.text = "Overview"
        slide2.placeholders[1].text = paper_desc

        # 3. Bullet Points Slide
        bullet_slide_layout = prs.slide_layouts[1]
        slide3 = prs.slides.add_slide(bullet_slide_layout)
        
        shapes = slide3.shapes
        title_shape = shapes.title
        body_shape = shapes.placeholders[1]
        
        title_shape.text = "Key Contributions & Findings"
        tf = body_shape.text_frame
        tf.clear()
        
        for bullet in bullets[:7]:
            p = tf.add_paragraph()
            p.text = bullet
            p.level = 0
            p.font.size = Pt(18)
            
        ppt_path = tmp_dir / f"{uuid.uuid4()}.pptx"
        prs.save(str(ppt_path))
        
        return {
            "description": paper_desc + "\n\n" + "\n".join(f"• {b}" for b in bullets),
            "ppt_url": f"/api/download/{ppt_path.name}"
        }
    except Exception as e:
        raise HTTPException(status_code=500, detail=str(e))
    finally:
        # Clean up PDF (keep PPT for download)
        if pdf_path.exists():
            pdf_path.unlink()

