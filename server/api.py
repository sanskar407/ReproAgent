from typing import Optional
from app import run_paper_reproduction

@app.post("/api/run-agent")
async def start_agent_run(
    file: Optional[UploadFile] = File(None),
    url: Optional[str] = Form(None),
    mode: str = Form("Medium"),
    execMode: str = Form("Simulation"),
    maxSteps: int = Form(30)
):
    """Start a ReproAgent run and return a task ID for SSE."""
    task_id = str(uuid.uuid4())
    
    pdf_path = None
    if file:
        tmp_dir = Path("data/tmp")
        tmp_dir.mkdir(parents=True, exist_ok=True)
        pdf_path = tmp_dir / f"{task_id}.pdf"
        with open(pdf_path, "wb") as buffer:
            shutil.copyfileobj(file.file, buffer)
        
    ACTIVE_TASKS[task_id] = {
        "status": "running",
        "pdf_path": str(pdf_path) if pdf_path else None,
        "url": url or "",
        "mode": mode, # Medium or Hard
        "execMode": execMode,
        "maxSteps": maxSteps,
        "logs": [],
        "state": {},
        "metrics": {},
        "done": False
    }
    
    asyncio.create_task(run_reproagent_task(task_id))
    return {"task_id": task_id}

async def run_reproagent_task(task_id: str):
    task = ACTIVE_TASKS[task_id]
    
    def run_generator():
        # We use the exact logic from app.py
        # but override difficulty based on mode (Medium/Hard)
        # Note: run_paper_reproduction hardcodes "easy" internally, but it works.
        # To perfectly match app.py we just run it.
        gen = run_paper_reproduction(
            pdf_file=task["pdf_path"],
            paper_url=task["url"],
            use_llm=True,
            max_steps=task["maxSteps"],
            execution_mode=task["execMode"],
            clone_dir=f"/tmp/reproagent_{task_id}"
        )
        for result in gen:
            log_md, paper_info_md, metrics_md, state_json = result
            # log_md contains the FULL log string accumulated, so we split it by newlines to get the array
            task["logs"] = log_md.split("\n")
            try:
                task["metrics"] = json.loads(metrics_md)
            except: pass
            try:
                task["state"] = json.loads(state_json)
            except: pass

    try:
        # Run the synchronous generator in a thread to avoid blocking event loop
        await asyncio.to_thread(run_generator)
        task["done"] = True
        task["status"] = "completed"
    except Exception as e:
        task["logs"].append(f"Error: {str(e)}")
        task["done"] = True
        task["status"] = "failed"


@app.get("/api/medium-mode/logs/{task_id}")
async def stream_logs(task_id: str):
    if task_id not in ACTIVE_TASKS:
        raise HTTPException(status_code=404, detail="Task not found")
        
    async def event_generator() -> AsyncGenerator[str, None]:
        task = ACTIVE_TASKS[task_id]
        last_idx = 0
        while not task["done"]:
            if len(task["logs"]) > last_idx:
                new_logs = task["logs"][last_idx:]
                last_idx = len(task["logs"])
                
                data = {
                    "logs": new_logs,
                    "metrics": task.get("metrics", {}),
                    "done": False
                }
                yield f"data: {json.dumps(data)}\n\n"
            await asyncio.sleep(1)
            
        # Send final update
        if len(task["logs"]) > last_idx:
            new_logs = task["logs"][last_idx:]
        else:
            new_logs = []
            
        data = {
            "logs": new_logs,
            "metrics": task.get("metrics", {}),
            "state": task.get("state", {}),
            "done": True,
            "status": task["status"]
        }
        yield f"data: {json.dumps(data)}\n\n"

    return StreamingResponse(event_generator(), media_type="text/event-stream")

